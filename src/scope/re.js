var firstName; //se está declarando. El valor por default es undefined, porque no es igual a nada
firstName = "Enrique";
console.log(firstName);

var lastName = "Palomino"; //declaración y asignación
lastName = "Ana"; // esta es una reasignación del valor de la variable lastName.
console.log(lastName);

var secondName = "David"; //declaración y asignación
var secondName = "Gilberto"; //acá se está aplicando la reasignación y redeclaración de la variable
console.log(secondName);

// uso de let
let fruit = "Apple"; //declaracion y asignación
fruit = "banana"; // reasignación

console.log(fruit);

// uso de const
const animal = "dog"; //declaración y asignación
